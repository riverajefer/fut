$k(document).ready(function () {
  $k('a[rel=optica]').fancybox({
    transitionIn: 'fade',
    overlayColor: '#000',
    overlayOpacity: 0.9,
    // titlePosition: 'over',
    titlePosition: 'outside',
    titleFormat: function (title, currentArray, currentIndex, currentOpts) {
      return (
        '<span id="fancybox-title-over">Proyectos ' +
        (currentIndex + 1) +
        ' de ' +
        currentArray.length +
        (title.length ? ' &nbsp; ' + title : '') +
        '</span>'
      );
    },
  });
});

